package com.ruslanhuseynov.simplecalculator

class Math() {

    fun plus(x : Double,y : Double) : Double {

        return (x + y)
    }

    fun minus(x: Double,y: Double) : Double {

        return (x - y)
    }

    fun multiply(x: Double,y: Double) : Double {

        return (x * y)
    }

    fun div(x: Double,y: Double) : Double {

        return (x / y)
    }
}